def fmt_num(value: float | int | Fraction) -> str:
    if isinstance(value, Fraction):
        if value.denominator == 1:
            return str(value.numerator)
        return f"{value.numerator}/{value.denominator}"
    if isinstance(value, int):
        return str(value)
    rounded = round(float(value), 4)
    if abs(rounded - round(rounded)) < 1e-9:
        return str(int(round(rounded)))
    return f"{rounded:.4f}".rstrip("0").rstrip(".")


def unique_distractors(correct: str, candidates: list[str]) -> list[str]:
    out: list[str] = []
    for item in candidates:
        item = str(item)
        if item != correct and item not in out:
            out.append(item)
        if len(out) == 3:
            return out
    seed = 1
    while len(out) < 3:
        candidate = f"{correct} ({seed})"
        if candidate not in out:
            out.append(candidate)
        seed += 1
    return out




def normalize_choice_units(correct: str, distractors: list[str]) -> list[str]:
    """Keep units visually consistent across all four options."""
    match = re.fullmatch(r"(-?\d+(?:\.\d+)?)\s+(.+)", str(correct).strip())
    if not match:
        return [str(x) for x in distractors]
    suffix = match.group(2).strip()
    allowed_markers = ("سم", "متر", "كم", "ساعة", "ساعات", "يوم", "أيام", "ريال", "لتر", "مرات", "مرة", "سنة", "سنوات")
    if not any(marker in suffix for marker in allowed_markers):
        return [str(x) for x in distractors]
    out=[]
    for item in distractors:
        text=str(item).strip()
        if re.fullmatch(r"-?\d+(?:\.\d+)?", text):
            text=f"{text} {suffix}"
        out.append(text)
    return out

def positioned_choices(correct: str, distractors: list[str], ordinal: int) -> tuple[list[str], int]:
    distractors = unique_distractors(correct, distractors)
    correct_index = ordinal % 4
    choices = distractors[:]
    choices.insert(correct_index, correct)
    return choices, correct_index


def qid_for(exam: str) -> str:
    COUNTERS[exam] += 1
    prefix = "QDR-CQ" if exam == "قدرات كمي" else "QDR-CV"
    return f"{prefix}-{COUNTERS[exam]:04d}"


def add_question(
    *, exam: str, category: str, skill: str, question: str,
    correct: str, distractors: list[str], explain: str,
    difficulty: str = "متوسط", keywords: list[str] | None = None,
    concept_id: str = "", misconception_id: str = "",
    passage: str = "", image: str = "", image_caption: str = "",
    source_group: str = "quant_daily", source_pattern: str = "",
    display_variant: str = "standard", time_sec: int = 60,
    diagnostic: bool = False,
) -> dict:
    qid = qid_for(exam)
    distractors = normalize_choice_units(str(correct), [str(x) for x in distractors])
    choices, correct_index = positioned_choices(str(correct), distractors, len(QUESTIONS))
    q = {
        "id": qid,
        "exam": exam,
        "test_format": "محوسب",
        "delivery_mode": "computerized",
        "eligible_tracks": ["علمي", "أدبي"],
        "category": category,
        "skill": skill,
        "subject": "قدرات",
        "unit": skill,
        "question": question.strip(),
        "choices": choices,
        "correct": correct_index,
        "answer": str(correct),
        "explain": explain.strip(),
        "difficulty": difficulty,
        "time_per_question_sec": time_sec,
        "diagnostic": diagnostic,
        "keywords": keywords or [skill],
        "concept_id": concept_id or f"qudrat.{('quant' if exam == 'قدرات كمي' else 'verbal')}.{skill.replace(' ', '_')}",
        "summary_block_id": "",
        "misconception_id": misconception_id,
        "display_variant": display_variant,
        "source_status": "transformed_original_v1",
        "source_documents": SOURCE_DOCS[source_group],
        "source_pattern": source_pattern or skill,
        "copyright_method": "new_wording_and_original_values",
        "editorial_status": "qa_passed_internal",
        "release_eligible": True,
        "rights_status": "original_transformation",
    }
    if passage:
        q["passage"] = passage.strip()
    if image:
        q["image"] = image
        q["image_caption"] = image_caption
        q["display_variant"] = "diagram"
    QUESTIONS.append(q)
    return q


