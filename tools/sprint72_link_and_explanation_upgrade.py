AR_DIAC = re.compile(r"[\u064B-\u065F\u0670]")
NONWORD = re.compile(r"[^\u0600-\u06FFa-zA-Z0-9]+")
SPACE = re.compile(r"\s+")


def norm(value: Any) -> str:
    text = unicodedata.normalize("NFKD", str(value or ""))
    text = AR_DIAC.sub("", text)
    text = (
        text.replace("أ", "ا")
        .replace("إ", "ا")
        .replace("آ", "ا")
        .replace("ة", "ه")
        .replace("ى", "ي")
        .replace("ـ", "")
    )
    return NONWORD.sub(" ", text).strip().lower()


def clean(value: Any) -> str:
    return SPACE.sub(" ", str(value or "").strip())


def canonical(value: Any) -> str:
    words = []
    for word in norm(value).split():
        if word.startswith("ال") and len(word) > 4:
            word = word[2:]
        words.append(word)
    return " ".join(words)


def tokens(value: Any) -> set[str]:
    out: set[str] = set()
    for word in norm(value).split():
        if len(word) >= 3:
            out.add(word)
            if word.startswith("ال") and len(word) > 4:
                out.add(word[2:])
    return out


