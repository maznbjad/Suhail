    bank: list[dict] = []
    quant_bank(bank)
    verbal_bank(bank)
    tahsili_bank(bank)
    assert len(bank) == 1000, len(bank)
    # Ensure exactly five diagnostic questions per track.
    for exam in ("قدرات كمي", "قدرات لفظي", "تحصيلي"):
        candidates = [q for q in bank if q["exam"] == exam]
        marked = [q for q in candidates if q.get("diagnostic")]
        if len(marked) < 5:
            for q in candidates:
                if not q.get("diagnostic"):
                    q["diagnostic"] = True
                    marked.append(q)
                    if len(marked) == 5:
                        break
        for q in marked[5:]:
            q["diagnostic"] = False
    OUT.write_text(json.dumps(bank, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {len(bank)} questions to {OUT}")


