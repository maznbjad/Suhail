def validate() -> dict:
    errors: list[str] = []
    seen_ids: set[str] = set()
    seen_text: set[str] = set()
    correct_positions = [0, 0, 0, 0]
    by_exam: dict[str, int] = {}
    by_category: dict[str, int] = {}
    for q in QUESTIONS:
        qid = q["id"]
        if qid in seen_ids: errors.append(f"duplicate id {qid}")
        seen_ids.add(qid)
        norm = " ".join(q["question"].split()) + "||" + " ".join(q.get("passage", "").split())
        if norm in seen_text: errors.append(f"duplicate text {qid}")
        seen_text.add(norm)
        if len(q["choices"]) != 4 or len(set(q["choices"])) != 4:
            errors.append(f"invalid choices {qid}: {q['choices']}")
        if q["choices"][q["correct"]] != q["answer"]:
            errors.append(f"answer mismatch {qid}")
        if not q["explain"]: errors.append(f"missing explain {qid}")
        if q["test_format"] != "محوسب": errors.append(f"wrong format {qid}")
        correct_positions[q["correct"]] += 1
        by_exam[q["exam"]] = by_exam.get(q["exam"], 0) + 1
        key = f"{q['exam']} / {q['category']}"
        by_category[key] = by_category.get(key, 0) + 1
        if q.get("image") and not (ROOT / q["image"]).exists():
            errors.append(f"missing image {qid}")
    expected = {"قدرات كمي": 195, "قدرات لفظي": 85}
    if by_exam != expected:
        errors.append(f"unexpected counts {by_exam}")
    return {
        "release": "58.0.0",
        "question_count": len(QUESTIONS),
        "by_exam": by_exam,
        "by_category": by_category,
        "correct_position_distribution": correct_positions,
        "unique_ids": len(seen_ids),
        "unique_question_passage_pairs": len(seen_text),
        "diagram_count": sum(bool(q.get("image")) for q in QUESTIONS),
        "errors": errors,
        "qa_passed": not errors,
        "notes": [
            "All quantitative values in this bank are newly authored and recomputed.",
            "Verbal questions and reading passages are newly written; source compilations were used only to identify test patterns.",
            "The bank is tagged computerized only and is not mixed with the future paper bank.",
        ],
    }


