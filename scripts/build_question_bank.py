def make_choices(correct: str, distractors: Iterable[str]) -> tuple[list[str], int]:
    values = [str(correct)] + [str(x) for x in distractors if str(x) != str(correct)]
    # Ensure four distinct choices.
    seen: list[str] = []
    for value in values:
        if value not in seen:
            seen.append(value)
    while len(seen) < 4:
        seen.append(f"{correct} ({len(seen) + 1})")
    choices = seen[:4]
    RNG.shuffle(choices)
    return choices, choices.index(str(correct))


def add(bank: list[dict], *, qid: str, exam: str, category: str, skill: str,
        question: str, correct: str, distractors: Iterable[str], explain: str,
        difficulty: str = "متوسط", subject: str = "", unit: str = "",
        diagnostic: bool = False) -> None:
    choices, index = make_choices(correct, distractors)
    bank.append({
        "id": qid,
        "exam": exam,
        "category": category,
        "skill": skill,
        "subject": subject,
        "unit": unit or skill,
        "question": question,
        "choices": choices,
        "correct": index,
        "answer": correct,
        "explain": explain,
        "difficulty": difficulty,
        "time_per_question_sec": 60,
        "diagnostic": diagnostic,
        "source_status": "development_seed",
    })


